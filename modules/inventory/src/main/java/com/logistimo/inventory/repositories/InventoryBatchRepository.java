package com.logistimo.inventory.repositories;

import com.logistimo.inventory.entity.jpa.InventoryBatch;

import org.springframework.data.jpa.repository.JpaRepository;
import org.springframework.data.jpa.repository.JpaSpecificationExecutor;
import org.springframework.data.jpa.repository.Query;

import java.util.List;

/**
 * Created by pratheeka on 25/07/18.
 */
public interface InventoryBatchRepository extends
    JpaRepository<InventoryBatch, String>,
    JpaSpecificationExecutor<InventoryBatch> {

  @Query(value = "select i from InventoryBatch i where i.kioskId=?1 and i.materialId in (?2)",nativeQuery = false)
  List<InventoryBatch> getInventoryBatches(Long kioskId, List<Long> materialIds);
}
